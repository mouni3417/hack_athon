                    <p class="mx-4">Consumer behavior plays a crucial role in food waste generation. Factors such as buying more food than needed, improper meal planning, and impulse purchases contribute to household food waste. Consumers may also discard food based on expiration dates, even if the food is still safe to eat, leading to unnecessary waste. Additionally, societal norms around food abundance and perfection contribute to a culture of excess and disposal.
                    </p><br>
                </div>
