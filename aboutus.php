                    <h1 class="text-blue-900  font-bold font-mono text-2xl mx-4 py-6" style="color: rgb(118, 67, 67);">Overproduction:</h1>
                    <p class="mx-4">One of the significant contributors to food waste is overproduction within the agricultural and food industries. Farmers often produce more crops or livestock than necessary to meet demand, anticipating potential losses due to weather, pests, or market fluctuations. When there is excess production, surplus food may go unsold or unused, leading to waste at the production stage.</p>
                </p><br>
                </div>
                <div class="md:container md:my-6  md:mx-5  md:bg-fuchsia-200 md:h-auto md:max-w-auto">
