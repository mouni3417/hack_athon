                        <p class="mx-4">
                
                            <b>Greenhouse Gas Emissions:</b> When food waste is sent to landfills, it decomposes anaerobically, producing methane, a potent greenhouse gas that contributes to climate change.
                            Resource Depletion: Food production requires vast amounts of land, water, energy, and other resources. When food is wasted, it represents a squandering of these valuable resources.
                            Biodiversity Loss: Agricultural activities associated with food production, such as deforestation and habitat destruction, can lead to loss of biodiversity and ecosystem degradation.</p> <br>
