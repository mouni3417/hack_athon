                        <p class="mx-4"><b>Financial Losses: </b>Food waste represents a significant economic loss throughout the supply chain, from farmers and producers to retailers and consumers. This includes the cost of producing, transporting, and storing food that ultimately goes to waste.
                            Reduced Profitability: Businesses in the food industry incur losses when products are discarded due to spoilage, expiration, or cosmetic imperfections. These losses can impact profitability and sustainability.
                            Waste Management Costs: Municipalities and waste management agencies bear the costs of collecting, transporting, and disposing of food waste in landfills or through other means.
